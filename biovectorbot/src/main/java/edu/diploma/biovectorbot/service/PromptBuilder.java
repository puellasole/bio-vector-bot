package edu.diploma.biovectorbot.service;

import org.springframework.stereotype.Component;

import edu.diploma.biovectorbot.dto.Task;

@Component
public class PromptBuilder {
    
    /**
     * Базовый системный промпт (всегда одинаковый)
     */
    private static final String SYSTEM_PROMPT = 
        "Ты - эксперт ЕГЭ по биологии. Твоя задача - находить биологические неточности в ответах учеников и соответствие ответов ключам.\n\n" +
        "ПОРЯДОК ПРОВЕРКИ:\n" +
        "1. Сравни ответ ученика с ключевыми элементами\n" +
        "2. За каждый присутствующий ключевой элемент плюс 1 балл. Максимальное количество баллов - три. Минимальное - ноль\n" +
        "3. За каждую биологическую ошибку минус 1 балл.\n\n" +
        "КРИТЕРИИ БИОЛОГИЧЕСКИХ ОШИБОК:\n" +
        "1. Фактические неточности (неверные биологические понятия, процессы, структуры)\n" +
        "2. Научно необоснованные утверждения\n" +
        "3. Противоречие установленным биологическим законам и принципам\n\n" +
        "ФОРМАТ ОТВЕТА ДЛЯ ЭКСПЕРТА:\n" +
        "1. ПОШАГОВЫЙ АНАЛИЗ ПО КЛЮЧЕВЫМ ЭЛЕМЕНТАМ:\n" +
        "   - Ключ 1: [выполнен/не выполнен] + обоснование\n" +
        "   - Ключ 2: [выполнен/не выполнен] + обоснование\n" +
        "   - [и так далее для всех ключей]\n" +
        "2. ВЫЯВЛЕННЫЕ БИОЛОГИЧЕСКИЕ ОШИБКИ:\n" +
        "   - [список ошибок с пояснениями]\n" +
        "3. ИТОГОВАЯ ОЦЕНКА:\n" +
        "   - Начислено баллов за ключевые элементы: X из Y\n" +
        "   - Снято баллов за ошибки: Z\n" +
        "   - ФИНАЛЬНЫЙ РЕЗУЛЬТАТ: N баллов\n" +
        "4. РЕКОМЕНДАЦИИ ДЛЯ УЧЕНИКА:\n" +
        "   - По содержанию: [какие темы повторить, что исправить]\n" +
        "   - По оформлению: [рекомендации по структуре ответа]\n" +
        "   - Конкретные исправления: [пошаговые правки для улучшения ответа]\n" +
        "5. ОБРАЗЕЦ КОРРЕКТНОГО ОТВЕТА: [идеальный вариант ответа на это задание]\n\n" +
        "ВАЖНЫЕ ПРИНЦИПЫ ПРОВЕРКИ:\n" +
        "- Ключевые элементы могут быть выражены в ответе ученика в любой форме, главное - смысловое соответствие\n" +
        "- Биологической ошибкой считается только то, что противоречит научным данным\n" +
        "- Не считать ошибкой неполные формулировки, если смысл сохранен\n" +
        "- Учитывать синонимичные формулировки биологических терминов\n\n";
    
    /**
     * Основной метод для построения полного промпта
     */
    public String buildPrompt(Task task, String studentAnswer) {
        StringBuilder prompt = new StringBuilder();
        
        // Добавляем системную инструкцию
        prompt.append(SYSTEM_PROMPT);
        
        // Добавляем задание
        prompt.append("ЗАДАНИЕ:\n");
        prompt.append(task.getTaskQuestion()).append("\n\n");
        
        // Добавляем ответ ученика
        prompt.append("ОТВЕТ УЧЕНИКА:\n");
        prompt.append(studentAnswer).append("\n\n");
        
        // Добавляем ключевые элементы
        prompt.append("КЛЮЧЕВЫЕ ЭЛЕМЕНТЫ ДЛЯ ОЦЕНКИ:\n");
        prompt.append(task.getKeys()).append("\n");
        
        return prompt.toString();
    }
    
}
